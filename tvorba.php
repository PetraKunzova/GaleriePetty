                     <h3>Tvořím v Apo</h3>
                  <hr>
                <p>Zaujala vás možnost vytvořit si fraktál v Apophysis? Pak se vás na této stránce pokusím provést ve stručnosti programem 
                 a jeho možnostmi z vlastních zkušeností. Upřesním ještě, že návod zde je  podle Apophysis 7x. 
                 Starší verze i tato mají základ společný, jen od 7x je možné si přidat pluginy. 
                 Pokud si některý chcete stáhnout, vzpomeňte, jaký máte systém. Poslední verzi totiž 
                 v pohodě otevřete až od Windows Vista. Vistu strpěl ale málokdo, tak tedy počítejme od Windows 7.
                 Najít zdroj, kde si jej stáhnout je lehká věc. Stačí zadat název programu a internet vám nabídne třeba Slunečnici, ulož.to, nebo stahuj.cz a jsou free, čili zdarma.
                </p>
                <br>
                <hr>
                <p>Otevřu si tedy Apophysis . V okně, které se mi zobrazilo, největší plochu zabírá první fraktál vytvořený (vygenerovaný) programem. Není to tedy uplně první, protože jsem si vybrala další z nabízených obrázků. 
                 Všimněte si rozdíl čísla v okénku, kam směřuje šipka. Tam jsem si totiž navolila výši jakou se mi obrázek vyčistil od šumu. Klikněte si na obrázky ať vidíte ten rozdíl.  
                 <br>
                 *A nejen u těchto dvou. Rozbalí se vám téměř všechny obrázky. ☺
